    private void ScheduleVisitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ScheduleVisitButtonActionPerformed
        if (validateFields() == true){
            
            String dateTime = DateTextField.getText() + "" + TimeTextField.getText();
            Visit visit = new Visit(this.customer.getId(),this.property.getId(),dateTime);

            ISystemFacade facade = new SystemFacade();
            if(facade.scheduleVisit(visit) == true){
                this.dispose();          
                this.previousWindow.setVisible(true);
            }
        }
    }//GEN-LAST:event_ScheduleVisitButtonActionPerformed

