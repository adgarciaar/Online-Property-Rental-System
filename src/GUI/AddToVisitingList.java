    private boolean validateFields(){
        
        boolean b = true;
        
        if(DateTextField.getText().compareTo("") == 0){      
            b = false; 
            JOptionPane.showMessageDialog(null, "You have to enter a date");   
        }else{
            DateValidator dateValidator = new DateValidator();
            if (dateValidator.validate(DateTextField.getText()) == false){
                b = false;
                JOptionPane.showMessageDialog(null, "Incorrect date");            
            }else{
                try {
                    DateFormat df = new SimpleDateFormat("dd/MM/yyyy");
                    Date dateobj = new Date();
                    
                    Date dateText=new SimpleDateFormat("dd/MM/yyyy").parse(DateTextField.getText());
                    
                    if (dateText.compareTo(dateobj) < 0 || dateText.compareTo(dateobj) == 0) {                                         
                        b = false;
                        JOptionPane.showMessageDialog(null, "Invalid date");       
                    }
                } catch (ParseException ex) {
                    Logger.getLogger(AddPhotoWindow.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        
        if(TimeTextField.getText().compareTo("") == 0){      
            b = false;
             JOptionPane.showMessageDialog(null, "You have to enter the time"); 
        }else{
            if (TimeTextField.getText().matches("([01]?[0-9]|2[0-3]):[0-5][0-9]") == false){
                b = false;
                JOptionPane.showMessageDialog(null, "Time is incorrect");
            }
        }      
        
        return b;
        
    }

